function isci_polno_vrstico(t) {
    for (let i = 1; t[i][1] != 0; i++) {
        let vsota_vrstice = 0
        let j = 1;
        for (; t[i][j] != 0; j++) {
            vsota_vrstice += t[i][j];
        }
        if (vsota_vrstice / 2 == j - 1) {
            return i;
        }
    }
    return -1
}




