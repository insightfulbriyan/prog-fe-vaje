function izvleci_poliomino(tetris, p) {
    let dolzina = 0;

	for(let y = 1; !!tetris[y]; y++){
		for(let x = 1; tetris[y][x] != 0; x++){
			if(tetris[y][x] == 3) {
                p[dolzina] = [y, x];
                dolzina++;
            }
		}
	}
    
    return dolzina;
}


