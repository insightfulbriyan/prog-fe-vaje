function brisi_vrstico(t, n) {
    // Prazna vrstica
    let prev = [0];
    let i = 1;
    for (; t[1][i] != 0; i++) {
        prev[i] = 1;
    }
    prev[i] = 0

    for (i = 1; i <= n; i++) {
        let temp = t[i];
        t[i] = prev;
        prev = temp;
    }
}

